export const SUN = 0;
export const MON = 1;
export const TUE = 2;
export const WED = 3;
export const THU = 4;
export const FRI = 5;
export const SAT = 6;

export const GLORIOUS = '@@glorious';
export const JOYFUL = '@@joyful';
export const LUMINOUS = '@@luminous';
export const SORROWFUL = '@@sorrowful';

export function whatMysteryTodayContemporary(): string {
  const dow = (new Date()).getDay();
  if (dow === THU) {
    return LUMINOUS;
  }
  return whatMysteryTodayTraditionalWorker(dow);
}

export function whatMysteryTodayTraditional(): string {
  const dow = (new Date()).getDay();
  return whatMysteryTodayTraditionalWorker(dow);
}

export function whatMysteryTodayTraditionalWorker(dow: number): string {
  if (dow === SUN || dow === WED) {
    return GLORIOUS;
  }
  if (dow === MON || dow === SAT || dow === THU) {
    return JOYFUL;
  }
  return SORROWFUL;
}
